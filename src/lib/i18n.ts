export function tKey(key: string, lang: Lang, vars?: Record<string, string>): string {
  const entry = dict[key];
  let str = entry ? (entry[lang] || entry.en || key) : key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{${k}}`, v);
    }
  }
  return str;
}
